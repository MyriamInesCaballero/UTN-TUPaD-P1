# Crear una lista vacía
dobles = []

# Agregar el doble de 5, 10 y 15 usando append. El doble de un número se calcula multiplicando por 2.
# Usamos append() para agregar de a uno cada valor:
dobles.append(5 * 2)
dobles.append(10 * 2)
dobles.append(15 * 2)

# Imprimir la lista resultante
print(dobles)

